    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $user = User::find($id);

        return view('auth.passwords.cambiar-pass')
            ->withUser($user);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, array(
            'pass_anterior' => 'required',
            'password' => 'required|min:8|same:password_repetir',
            'password_repetir' => 'required'
        ));
        $user = User::find($id);

        if( Hash::check($request->pass_anterior,$user->password) ){
            $user->password = bcrypt($request->password);
            $user->save();

            Session::flash('success', "Contraseña cambiada del usuario ".$user->username."cambiada correctamente...");
            Auth::logout();
            return redirect('/');
        }else{
            return redirect()->back()->withErrors(['La contraseña actual no coincide']);
        }
    }

